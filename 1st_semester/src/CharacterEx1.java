public class CharacterEx1 {
  public static void main(String[] args) {
    char ch1 = 'A';
    System.out.println("ch1 = " + ch1);
    System.out.println("ch1 = " + (int) ch1);

    char ch2 = 66;
    System.out.println("ch2 = " + ch2);

    char ch3 = 67;
    System.out.println("ch3 = " + ch3);
    System.out.println("ch3 = " + (char) ch3);
  }
}